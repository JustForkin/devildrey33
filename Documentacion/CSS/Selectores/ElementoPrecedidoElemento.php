<p>Este selector nos permite especificar que queremos seleccionar un elemento que está precedido de otro elemento.</p>
<p>Estos elementos pueden ser etiquetas, clases, ID's, o cualquier tipo de selector permitido.</p>
<div class='nota'>Todos los Elemento2 que se encuentren después del Elemento1 serán seleccionados.</div><br />
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'><i>.Elemento ~ #Elemento</i> { };</pre>
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores compatibles con CSS3.</p>
