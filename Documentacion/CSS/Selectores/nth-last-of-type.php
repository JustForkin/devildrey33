<p>Este selector selecciona el elemento especificado (del mismo tipo) partiendo de la posición del hijo, contando por detrás.</p>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'><i>Elemento</i>:nth-last-of-type(<i>Posición</i>) { };</pre>
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores compatibles con CSS3.</p>
