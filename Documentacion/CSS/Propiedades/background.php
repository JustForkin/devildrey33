<p>Propiedad corta que nos permite especificar una o mas propiedades para el fondo.</p>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'>background: <b>valores</b>;</pre>
<table class='Tabla'>
    <tr>
        <td><b><code><a href="background-attachment">background-attachment</a></code></b></td>
        <td>Permite especificar como debe comportarse la imagen cuando el usuario utiliza el scroll del documento.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-color">background-color</a></code></b></td>
        <td>Permite especificar un color para el fondo.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-image">background-image</a></code></b></td>
        <td>Permite especificar una url con una imagen de fondo para mostrar.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-position">background-position</a></code></b></td>
        <td>Permite especificar en que posición empezara la imagen de fondo.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-repeat">background-repeat</a></code></b></td>
        <td>Permite especificar si la imagen de fondo debe repetirse.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-clip">background-clip</a></code></b></td>
        <td>Permite especificar el area donde se pintara el fondo dentro del objeto.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-origin">background-origin</a></code></b></td>
        <td>Permite especificar donde empezara a pintarse la imagen del fondo dentro del objeto.</td>
    </tr>
    <tr>
        <td><b><code><a href="background-size">background-size</a></code></b></td>
        <td>Permite especificar el tamaño con el que queremos mostrar la imagen de fondo, independientemente de su tamaño original.</td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores compatibles con CSS1, para <code><a href="background-clip">background-clip</a></code>, <code><a href="background-origin">background-origin</a></code>, <code><a href="background-size">background-size</a></code>, se requiere un navegador compatible con CSS3 (tambien se soportan estas porpiedades en IE9 y superiores).</p>
