<p>Esta propiedad se usa para retardar el inicio de la animación durante el tiempo especificado. El tiempo se puede especificar en segundos (s) o milisegundos (ms).</p>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'>animation-delay: <b>Tiempo</b>;</pre>
<table class='Tabla'>
    <tr>
        <td><b>Tiempo</b></td>
        <td>Tiempo en segundos(s) o milisegundos(ms) que tardará en arrancar la animación. El valor por defecto es 0.</td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores, aunque Chrome, Safari y Opera siguen requiriendo el prefijo <code>-webkit-</code>.</p>
