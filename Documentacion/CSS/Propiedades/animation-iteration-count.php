<p>Esta propiedad define el numero de veces que se repetirá la animación.</p>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'>animation-iteration-count: <b>Valor</b>;</pre>
<table class='Tabla'>
    <tr>
        <td><b>Valor</b></td>
        <td>Especifica el total de veces que se repetirá la animación, se puede especificar "<i>infinite</i>" para que la animación no se pare nunca. Por defecto 1.</td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores, aunque Chrome, Safari y Opera aun requieren el prefijo <code>-webkit-</code>.</p>
