<p>Esta propìedad nos permite especificar la alineación vertical para un objeto.</p>
<div class='nota'>Dependiendo del valor de la propiedad <a href="display" target="_blank">display</a> la propiedad vertical-align puede ser ignorada.</div>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'>vertical-align: <b>valor</b>;</pre>
<table class='Tabla'>
    <tr>
        <td style='width:100px'><b>baseline</b></td>
        <td>Alinéa la linea basica del elemento con la linea base del elemento padre. <b>Este es el valor por defecto.</b></td>
    </tr>
    <tr>
        <td><b>sub</b></td>
        <td>Alinéa el objeto como si fuera subíndice.</td>
    </tr>
    <tr>
        <td><b>super</b></td>
        <td>Alinéa el objeto como si fuera superíndice.</td>
    </tr>
    <tr>
        <td><b>top</b></td>
        <td>La parte superior del objeto se alinea con la parte superior del objeto más alto de la línea.</td>
    </tr>
    <tr>
        <td><b>text-top</b></td>
        <td>La parte superior del objeto se alinea con la parte superior de la fuente del objeto padre.</td>
    </tr>
    <tr>
        <td><b>middle</b></td>
        <td>El objeto se coloca en el medio del objeto padre.</td>
    </tr>
    <tr>
        <td><b>bottom</b></td>
        <td>La parte inferior del objeto está alineado con el objeto más bajo en la línea.</td>
    </tr>
    <tr>
        <td><b>text-bottom</b></td>
        <td>La parte inferior del objeto está alineado con la parte inferior de la fuente del objeto padre.</td>
    </tr>
    <tr>
        <td><i>valor</i></td>
        <td>Posiciona el elemento segun el valor. Se permiten valores negativos. Si se usan porcentajes, estos equivalen al mismo porcentaje de la propiedad <a href="line-height" target="_blank">line-height</a>.</td>
    </tr>
    <tr>
        <td><b>inherit</b></td>
        <td>Se heredara el estilo de su objeto padre. <div class='nota'>Este valor no está soportado en IE7 e inferiores. Para IE8 se necesita definir un <i>!DOCTYPE</i>.</div></td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores compatibles con CSS1.</p>
