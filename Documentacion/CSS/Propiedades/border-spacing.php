<p>Esta propiedad nos permite especificar el tamaño del espacio entre celdas. Podemos especificar el espacio horizontal y el espacio vertical por separado, o usar un solo valor para horizontal y vertical.</p>
<hr />
<h2>Sintaxis</h2>
<pre class='Sintaxis'>border-spacing: <b>valor</b>;</pre>
<table class='Tabla'>
    <tr>
        <td><b>valor</b></td>
        <td>Especifica un valor en unidades válidas para asignarlo al espacio horizontal y al espacio vertical. Para más información consulta la <a href="/Doc/CSS/Unidades/" target="_blank" title='px, ex, em, %, cm, mm, in, pt, pc'>Referencia CSS : Unidades</a>.</td>
    </tr>
    <tr>
        <td><b>valor valor</b></td>
        <td>Especifica el primer valor para el espacio horizontal, y el segundo valor para el espacio vertical en unidades válidas. Para más información consulta la <a href="/Doc/CSS/Unidades/" target="_blank" title='px, ex, em, %, cm, mm, in, pt, pc'>Referencia CSS : Unidades</a>.</td>
    </tr>
    <tr>
        <td><b>inherit</b></td>
        <td>Especifica que esta propiedad debe ser heredada desde su objeto padre. 
            <div class='nota'>IE8 soporta border-spacing solo si se define un <code>!DOCTYPE</code>.</div>
        </td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores compatibles con CSS2.<br />
<b>IE8</b> nefcesita tener definido un <code>!DOCTYPE</code>.</p>
