<p>Esta propiedad nos permite especificar el método que se usara para justificar el texto.</p>
<div class='nota'>Para utilizar esta propiedad debes establecer el valor de la propiedad <a href="text-align" target="_blank">text-align</a> a <code>justify</code>.</div>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'>text-justify: <b>Valor</b>;</pre>
<table class='Tabla'>
    <tr>
        <td style='min-width:130px;'><b>auto</b></td>
        <td>El navegador decide el algoritmo para la justificación.</td>
    </tr>
    <tr>
        <td><b>distribute</b></td>
        <td>Controla el espaciado de un modo muy parecido al valor de newspaper . Esta forma de justificación está optimizada para los documentos en idiomas de Asia, como el tailandés.</td>
    </tr>
    <tr>
        <td><b>inter-cluster</b></td>
        <td>Justifica las líneas de texto que no contienen espaciado entre palabras. Esta forma de justificación está optimizada para documentos en idiomas de Asia.</td>
    </tr>
    <tr>
        <td><b>inter-ideograph</b></td>
        <td>Justifica las líneas de texto ideográfico y aumenta o disminuye el espaciado entre ideogramas o entre palabras.</td>
    </tr>
    <tr>
        <td><b>inter-word</b></td>
        <td>Justifica el texto aumentando el espacio entre palabras. El comportamiento de espaciado de este valor es la forma más rápida de hacer que todas las líneas de texto sean de igual longitud. Su comportamiento de justificación no afecta a la última línea del párrafo.</td>
    </tr>
    <tr>
        <td><b>kashida</b></td>
        <td>Justifica líneas de texto alargando caracteres en puntos seleccionados. Esta forma de justificación está diseñada para idiomas de escritura árabe.</td>
    </tr>
    <tr>
        <td><b>newspaper</b></td>
        <td>Aumenta o disminuye el espaciado entre letras y entre palabras. Es la forma más sofisticada de justificación de alfabetos latinos.</td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad unicamente está soportada en Internet Explorer.</p>
