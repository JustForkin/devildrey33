<p>Esta propiedad permite establecer la distancia entre el borde interior y el borde exterior del objeto. El borde interior se especifica con la propiedad "<a href="border"><i>border</i></a>".</p>
<hr />
<h2>Sintaxis</h2>
<pre class='devildrey33_Marco Pre_Sintaxis'>outline-offset: <b>Valor</b>;</pre>
<table class='Tabla'>
    <tr>
        <td><b>Valor</b></td>
        <td>Distancia entre el borde interior y el borde exterior.</td>
    </tr>
    <tr>
        <td><b>inherit</b></td>
        <td>Se heredara el estilo de su objeto padre.</td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad es soportada por todos los navegadores compatibles con CSS3.</p>
<div class='nota'>Internet explorer 9 no es compatible con CSS3.</div>
