<p>Esta propiedad nos permite especificar de que forma se mostraran los caracteres, con mayusculas y minusculas normales, o convirtiendo todos los caracteres a mayusculas y haciendo mas pequeños aquellos que representan minusculas.</p>
<hr />
<h2>Sintaxis</h2>
<pre class='Sintaxis'>font-variant: <b>valor</b> ;</pre>
<table class='Tabla'>
    <tr>
        <td><b>normal</b></td>
        <td>Se usaran mayusculas y minusculas normalmente.</td>
    </tr>
    <tr>
        <td><b>small-caps</b></td>
        <td>Se convertira todo el texto a mayusculas, y los caracteres que representan minusculas se mostraran mas pequeños.</td>
    </tr>
    <tr>
        <td><b>inherit</b></td>
        <td>Esta propiedad se heredara del objeto padre.
        	<div class='nota'>Este valor no se soporta en IE7, y en IE8 requiere la declaración de un <code>!DOCTYPE</code>.</div>
        </td>
    </tr>
</table><br />
<hr />
<h2>Compatibilidad</h2>
<p>Esta regla es soportada por todos los navegadores compatibles con CSS1.</p>
