<p>Propiedad que permite modificar la posición del objeto transformado.</p>
<br />
<h2>Sintaxis</h2>
<pre class='Sintaxis'>transform-origin: X Y Z;</pre>
<table class='Tabla'>
    <tr>
        <td><b>X</b></td>
        <td>Valor que define el eje X, y puede ser : <code>left</code>, <code>center</code>, <code>right</code>, o un valor con una <a href="/Doc/CSS/Unidades/" target="_blank" title='px, ex, em, %, cm, mm, in, pt, pc'>unidad</a> especifica.</td>
        <td>(por defecto <b>50%</b>)</td>
    </tr>
    <tr>
        <td><b>Y</b></td>
        <td>Valor que define el eje Y, y puede ser : <code>top</code>, <code>center</code>, <code>bottom</code>, o un valor con una <a href="/Doc/CSS/Unidades/" target="_blank" title='px, ex, em, %, cm, mm, in, pt, pc'>unidad</a> especifica.</td>
        <td>(por defecto <b>50%</b>)</td>
    </tr>
    <tr>
        <td><b>Z</b> (opcional)</td>
        <td>Valor que define el eje Z. Debes especificar un tipo de <a href="/Doc/CSS/Unidades/" target="_blank" title='px, ex, em, %, cm, mm, in, pt, pc'>unidad</a>. </td>
        <td>(por defecto <b>0</b>)</td>
    </tr>

</table><br />
<div class='nota'>Todas las funciones 3d requieren una perspectiva en el mismo objeto, o en el contenedor padre.</div>
<hr />
<h2>Compatibilidad</h2>
<p>Esta propiedad está soportada por todos los navegadores compatibles con CSS3.</p>
