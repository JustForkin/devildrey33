<h1>Blanco</h1>
<p>Blanco es un color acromático, de claridad máxima y de oscuridad nula, que se percibe como consecuencia de la fotorrecepción de una luz intensa constituida por todas las longitudes de onda del espectro visible, por tres longitudes de onda (larga, media y corta) o por dos longitudes de onda complementarias. Se asemeja al color de la nieve, aunque otras sustancias de máxima reflectancia, como la magnesia y la baritina (sulfato de bario), resultan ejemplos más específicos del color blanco.2</p>
<p>El color blanco que se encuentra estandarizado en catálogos de colores e inventarios cromáticos responde a la definición dada más arriba, es decir, es de claridad máxima y acromático. La denominación de color «blanco» incluye a las coloraciones similares al blanco estándar, denominadas blanquecinas o blancuzcas, que poseen una ligera sugerencia de saturación y matiz.2</p>
