<h1>Azul</h1>
<p>El azul es el color que se percibe ante la fotorrecepción de una luz cuya longitud de onda mide entre 460 y 482 nm. Se asemeja a la coloración más característica del lapislázuli.1</p>
<p>Se encuentra estandarizado en catálogos de colores e inventarios cromáticos, sirviendo este azul «estándar» como modelo y referente para el color azul.1 El azul estándar puede verse en el recuadro de la derecha, arriba; los valores que se dan debajo corresponden al mismo. La denominación de color «azul» abarca un conjunto de coloraciones similares al color estándar, azuladas.1</p>
