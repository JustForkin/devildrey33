<h1>Amarillo</h1>
<p>El amarillo es el color que se percibe ante la fotorrecepción de una luz cuya longitud de onda dominante mide entre 574 y 582 nm. Se asemeja a la coloración característica de la piel del limón maduro, de la flor del diente de león o del oro.2</p>
<p>Se encuentra estandarizado en catálogos de colores e inventarios cromáticos, sirviendo este amarillo «estándar» como modelo y referente para el color amarillo.2 El amarillo estándar puede verse en el recuadro de la derecha, arriba; los valores que se dan debajo corresponden al mismo.</p>
